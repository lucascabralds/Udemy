print("Essa é uma 'string' (str).") # o sistema de print no python ele sempre interliga como ela foi aberta,logo se ela abriu com aspas simples ela vai fechar com a proxima aspas simples
print('Essa é uma "string" (str).')  #A mesma se aplica com aspas duplas
print('Essa é uma  \'string\' (str).') #Caso queira apresentar algum valor, alguma string pode ser usado a barra e invertida, mas não é recomendado pois o codigo fica confuso.
print("Essa é uma  \"string\" (str).")
print('Esse é meu \n (str.')  #A sintaxe /n e usada em uma quebra de linha.
print(r'Esse é meu \n (str.')  #caso queira apresenta alguma sintaxe e nao quer que essa linha não seja executado nada, use a palavra r antes das aspas.

print('Essa é uma "string" (str).')  #Caso queira apresenta alguma string e recomendado a abrir o print com simples e apresenta alguma sintaxe com aspas duplas.